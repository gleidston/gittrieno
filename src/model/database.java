package model;

public class database {

  String lol ;
  String nome ;
  int numero ;
  int cpf ;
  double total ;
  float  clal ;

  public String getLol() {
    return lol;
  }

  public void setLol(String lol) {
    this.lol = lol;
  }

  public String getNome() {
    return nome;
  }

  public void setNome(String nome) {
    this.nome = nome;
  }

  public int getNumero() {
    return numero;
  }

  public void setNumero(int numero) {
    this.numero = numero;
  }

  public int getCpf() {
    return cpf;
  }

  public void setCpf(int cpf) {
    this.cpf = cpf;
  }

  public double getTotal() {
    return total;
  }

  public void setTotal(double total) {
    this.total = total;
  }

  public float getClal() {
    return clal;
  }

  public void setClal(float clal) {
    this.clal = clal;
  }
}
